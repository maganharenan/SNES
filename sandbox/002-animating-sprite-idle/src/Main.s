; ---- Export ------------------------------------------------------------------
.export ResetHandler
.export NMIHandler
; ------------------------------------------------------------------------------

; ---- Assembler Directives ----------------------------------------------------
.p816
; ------------------------------------------------------------------------------

; ---- NMI Handler -------------------------------------------------------------
.proc   NMIHandler
        lda     RDNMI

        tsx
        pea     OAMMIRROR
        jsr     UpdateOAMRAM
        txs

        rti
.endproc
; ------------------------------------------------------------------------------

; ---- IRQ Handler -------------------------------------------------------------
.proc   IRQHandler
        rti
.endproc
; ------------------------------------------------------------------------------